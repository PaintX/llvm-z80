#ifndef LLVM_LIB_TARGET_Z80_INSTPRINTER_Z80INSTPRINTER_H
#define LLVM_LIB_TARGET_Z80_INSTPRINTER_Z80INSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {
  class Z80InstPrinter final : public MCInstPrinter {
  public:
    Z80InstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
		   const MCRegisterInfo &MRI)
        : MCInstPrinter(MAI, MII, MRI) {}
  
    void printRegName(raw_ostream &OS, unsigned RegNo) const override;
    void printInst(const MCInst *MI, raw_ostream &OS, StringRef Annot,
		   const MCSubtargetInfo &STI) override;

    // Autogenerated by tblgen.
    void printInstruction(const MCInst *MI, raw_ostream &OS);
    static const char *getRegisterName(unsigned RegNo);

    void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &OS);

    void printImmMem(const MCInst *MI, unsigned OpNo, raw_ostream &OS);
    void printRegOffAddr(const MCInst *MI, unsigned OpNo, raw_ostream &OS);
    void printRegOffMem(const MCInst *MI, unsigned OpNo, raw_ostream &OS);
  };
}

#endif
